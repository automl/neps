import math

# The task: treat Branin input as hyperparameters optimized by NePS
def branin(x1, x2):
  """ The 2-dimensional Branin function.

  https://www.sfu.ca/~ssurjano/branin.html
  """
  a = 1
  b = 5.1 / (4 * math.pi**2)
  c = 5 / math.pi
  r = 6
  s = 10
  t = 1 / 8 * math.pi

  f = a * (x2 - b * x1**2 + c * x1 - r) ** 2 + s * (1 - t) * math.cos(x1) + s
  return f

# The search space
pipeline_space = dict(
    x1=neps.Float(lower=-5, upper=10),
    x2=neps.Float(lower=0, upper=15)
)

# The optimization
neps.run(
    evaluate_pipeline=branin,
    pipeline_space=pipeline_space,
    root_directory="branin_demo/",
    evaluations_to_spend=10,
)

