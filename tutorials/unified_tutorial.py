"""### Optimizing over multiple-objectives with priors and multi-fidelity

In Deep Learning it is often necessary to optimize over multiple objectives.

So far, we have been running HPO over a single objective: `1 - validation_accuracy`. We now optimize over 2 objectives by adding the `cost` objective.

Furthermore, we now define priors over each of the 2 objectives.

We use the same evaluation pipeline as seen previously and merge the existing objective with the cost to create a multi-objective function
"""

# Augmenting the evaluation pipeline with a cost objective
def mo_evaluate_pipeline(
    pipeline_directory,  # For saving checkpoints
    previous_pipeline_directory,  # For loading checkpoints
    **hyperparameters,  # Determined by HPO algorithms and passed by NePS
):
    result = training_pipeline(
        **hyperparameters,
        out_dir=pipeline_directory,
        load_dir=previous_pipeline_directory,
        # misc settings
        batch_size=512,
        log_neps_tensorboard=True,
        verbose=False,
        allow_checkpointing=True,
        use_for_demo=True  # toggle as desired
    )
    m_objective = [result["objective_to_minimize"], result["cost"]]
    result["objective_to_minimize"] = m_objective
    return result

"""We inject centers for Gaussian Priors over the two objectives, in the multi-fidelity pipeline space seen before."""

prior_centers = {
    "val_error": {
        "learning_rate": 0.01,
        "optimizer": "adamw",
        "num_neurons": 512,
    },
    "cost": {
        "learning_rate": 0.3,
        "optimizer": "adamw",
        "num_neurons": 128,
    }
}

"""Running multi-objective HPO with priors over multiple objectives using PriMO"""

set_seeds(1)
start = time.time()
neps.run(
    evaluate_pipeline=mo_evaluate_pipeline,  # edit here to change task to optimize
    root_directory="results_mohpo_priors",
    overwrite_root_directory=True,
    pipeline_space=pipeline_space_mf,
    fidelities_to_spend=100,
    optimizer=("primo", {"prior_centers": prior_centers})
)

