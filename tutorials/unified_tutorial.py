"""### ... with multi-fidelity

The previous setup ran each configuration for the number of `epochs` specified.

However, speedup techniques exploit the notion of _early stopping_ to terminate poor performing configurations by training them for an `epoch` <= `epochs` specified.

Such evaluation of a hyperparameter configuration at a lower epoch than desired is termed as a _cheaper proxy_ or a _lower fidelity_ evaluation.

To allow this, we add `epochs` into our search space and mark it as a _fidelity_.
"""

# Create a new fidelity-enabled search space
