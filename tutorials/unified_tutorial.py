"""## Speeding up ...

### ... with parallelization

All code you have seen would already be parallelizable, simply by executing it twice in parallel!

I.e., you can start multiple `neps.run()` from different processes however you like and they will synchronize, as long as they share the same `root_directory=`.

Say you have a script `worker.py` as

```python
# worker.py
neps.run(
    run_pipeline=...,
    pipeline_space=...,
    root_directory="some/path",
    evaluations_to_spend=100,
    max_evaluations_per_run=10,  # Each worker will only run 10 evaluations
)
```

Then, to spawn three parallel workers, you can run
```bash
python worker.py &
python worker.py &
python worker.py &
```

The same magic applies in order to continue HPO on a task: repeating the same call with a higher HPO budget suffices.
"""

